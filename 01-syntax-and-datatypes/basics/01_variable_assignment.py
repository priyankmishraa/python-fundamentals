# Script: Understanding Variables and Assignment in Python

# Explanation: What is a variable?
# A variable is a named storage location in the computer's memory where we can store and retrieve data.

# Explanation: How is a variable assigned?
# In Python, you can assign a value to a variable using the assignment operator '='.

# Example 1: Assigning a value to a variable
name = "John"
age = 25

# Example 2: Printing the values of the variables
print("Name:", name)
print("Age:", age)

# Explanation: The type() function
# The type() function is used to get the type of a variable or a value.

# Example 3: Using the type() function
print("Type of name:", type(name))
print("Type of age:", type(age))

# Explanation: Variables can change types
# In Python, variables can change types during the program's execution.

# Example 4: Changing the value and type of a variable
age = "Twenty-Five"
print("Updated Age:", age)
print("Updated Type of age:", type(age))

# --------------------
# OUTPUT
# --------------------

# Name: John
# Age: 25
# Type of name: <class 'str'>
# Type of age: <class 'int'>
# Updated Age: Twenty-Five
# Updated Type of age: <class 'str'>
