# Script: Exploring Arithmetic Operators in Python

# Explanation: Arithmetic Operators
# Arithmetic operators perform mathematical operations on numeric values.

# Example 1: Addition
a = 10
b = 5
sum_result = a + b
print("Addition:", sum_result)

# Example 2: Subtraction
difference_result = a - b
print("Subtraction:", difference_result)

# Example 3: Multiplication
product_result = a * b
print("Multiplication:", product_result)

# Example 4: Division
division_result = a / b
print("Division:", division_result)

# Example 5: Remainder (Modulus)
remainder_result = a % b
print("Remainder (Modulus):", remainder_result)

# Example 6: Exponentiation
exponentiation_result = a ** b
print("Exponentiation:", exponentiation_result)


# --------------------
# OUTPUT
# --------------------

# Addition: 15
# Subtraction: 5
# Multiplication: 50
# Division: 2.0
# Remainder (Modulus): 0
