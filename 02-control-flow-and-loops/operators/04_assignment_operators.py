
# Script: Understanding Assignment Operators in Python

# Explanation: Assignment Operators
# Assignment operators are used to assign values to variables.

# Example 1: Assignment (=) Operator
x = 5
print("Value of x after assignment:", x)

# Example 2: Addition Assignment (+=) Operator
x += 3  # Equivalent to: x = x + 3
print("Value of x after addition assignment:", x)

# Example 3: Subtraction Assignment (-=) Operator
x -= 2  # Equivalent to: x = x - 2
print("Value of x after subtraction assignment:", x)

# Example 4: Multiplication Assignment (*=) Operator
x *= 4  # Equivalent to: x = x * 4
print("Value of x after multiplication assignment:", x)

# Example 5: Division Assignment (/=) Operator
x /= 2  # Equivalent to: x = x / 2
print("Value of x after division assignment:", x)

# Example 6: Modulus Assignment (%=) Operator
x %= 3  # Equivalent to: x = x % 3
print("Value of x after modulus assignment:", x)

# Example 7: Exponentiation Assignment (**=) Operator
x **= 2  # Equivalent to: x = x ** 2
print("Value of x after exponentiation assignment:", x)


# --------------------
# OUTPUT
# --------------------

# Value of x after assignment: 5
# Value of x after addition assignment: 8
# Value of x after subtraction assignment: 6
# Value of x after multiplication assignment: 24
# Value of x after division assignment: 12.0
# Value of x after modulus assignment: 0.0
# Value of x after exponentiation assignment: 0.0
