
# Script: Infinite Loops and Practical Uses

# Explanation: Infinite Loops
# An infinite loop is a loop that runs indefinitely without a specific exit condition.

# Example 1: Using enumerate with a list
my_list = ['apple', 'banana', 'orange']

for index, fruit in enumerate(my_list):
    print(f"Index: {index}, Fruit: {fruit}")

# Index: 0, Fruit: apple
# Index: 1, Fruit: banana
# Index: 2, Fruit: orange

# Example 2: Using enumerate with a string
my_string = "Hello"

for index, char in enumerate(my_string):
    print(f"Index: {index}, Character: {char}")

# Index: 0, Character: H
# Index: 1, Character: e
# Index: 2, Character: l
# Index: 3, Character: l
# Index: 4, Character: o
